package Model;

public class Roles {
	
	private int ID;
	private String Name;
	private String Permissions;
	
	public int getID() {
		return ID;
	}
	public void setID(int iD) {
		ID = iD;
	}
	public String getName() {
		return Name;
	}
	public void setName(String name) {
		Name = name;
	}
	public String getPermissions() {
		return Permissions;
	}
	public void setPermissions(String permissions) {
		Permissions = permissions;
	}

}
